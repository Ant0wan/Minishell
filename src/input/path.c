#include <unistd.h>

char	*short_path(char **cwd)
{
	int		i;

	i = 0;
	*cwd = getcwd(NULL, 0);
	while ((*cwd)[i])
		++i;
	if (i > 0)
		--i;
	while ((*cwd)[i] != '/')
		--i;
	++i;
	return (&(*cwd)[i]);
}
